fun main()
{
    println("Hello World")
    var a:Int =10
    var b:String="hello"
    var c:Double=21.23
    var d:Float=11.2f
    var e:Long=2235664
    var f:Char='T'
    var g:Short=-2
    var h:Boolean=true
    var i:Byte=2
    println("$a is integer value")
    println("$b is string value")
    println("$c is double value")
    println("$d is float value")
    println("$e is long value")
    println("$f is character value")
    println("$g is short value")
    println("$h is boolean value")
    print("$i is byte value")
}